            }

            return decryptedBytes;
        }

        public void DecryptFile(string filename, string passw)
        {
            string fileEncrypted = filename; //"C:\\SampleFileEncrypted.DLL";
            string password = passw;

            byte[] bytesToBeDecrypted = File.ReadAllBytes(fileEncrypted);
            byte[] passwordBytes = Encoding.UTF8.GetBytes(password);
            passwordBytes = SHA256.Create().ComputeHash(passwordBytes);

            byte[] bytesDecrypted = AES_Decrypt(bytesToBeDecrypted, passwordBytes);

            string file = filename; //"C:\\SampleFile.DLL";
            File.WriteAllBytes(file, bytesDecrypted);
        }

        public string EncryptFile(string filename, string passw)
        {
            string file = filename;
            string password = passw;

            byte[] bytesToBeEncrypted = File.ReadAllBytes(file);
            byte[] passwordBytes = Encoding.UTF8.GetBytes(password);

            // Hash the password with SHA256
            passwordBytes = SHA256.Create().ComputeHash(passwordBytes);

            byte[] bytesEncrypted = AES_Encrypt(bytesToBeEncrypted, passwordBytes);

            string fileEncrypted = filename;

            File.WriteAllBytes(fileEncrypted, bytesEncrypted);
            return CalculateMD5Hash(bytesEncrypted);
        }

